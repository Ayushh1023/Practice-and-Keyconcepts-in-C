#include <stdio.h>
int main()
{
    printf("Hello World\n");
    int a = 2;
    float b = 4.56;
    char c = 'h';
    int d = 47;
    printf("The value of a is %d\n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);
    printf("The sum of a+d is %d\n", a + d);
    return 0;
}